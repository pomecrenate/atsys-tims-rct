package com.atsys.nxf.core.file;

import java.util.Locale;

import org.springframework.context.MessageSource;

import com.atsys.nxf.core.exception.NxfException;

public class NxfFileException extends NxfException {

	private static final long serialVersionUID = -8810278521369151292L;

	public NxfFileException(Exception exception) {
		super(exception);
	}

	public NxfFileException(MessageSource messageSource, String messageKey,
			Object[] messageParams) {
		super(messageSource, messageKey, messageParams);
	}

	public NxfFileException(MessageSource messageSource, String messageKey,
			Exception exception) {
		super(messageSource, messageKey, exception);
	}

	public NxfFileException(MessageSource messageSource, String messageKey,
			Exception exception, Locale locale) {
		super(messageSource, messageKey, exception, locale);
	}

	public NxfFileException(MessageSource messageSource, String messageKey,
			Object[] messageParams, Exception exception) {
		super(messageSource, messageKey, messageParams, exception);
	}

	public NxfFileException(MessageSource messageSource, String messageKey,
			Object[] messageParams, Exception exception, Locale locale) {
		super(messageSource, messageKey, messageParams, exception, locale);
	}

	public NxfFileException(MessageSource messageSource, String messageKey,
			Object[] messageParams, String defaultMessage, Exception exception) {
		super(messageSource, messageKey, messageParams, defaultMessage, exception);
	}

	public NxfFileException(MessageSource messageSource, String messageKey,
			Object[] messageParams, String defaultMessage, Exception exception,
			Locale locale) {
		super(messageSource, messageKey, messageParams, defaultMessage, exception,
				locale);
	}


}
